# Deskripsi: menghitung nilai pangkat 3 dari sebuah masukan integer

# Defenisi dan spesifikasi dari fungsi pangkat3 bernama fx3v1 adalah:
# fx3v1 : integer --> integer
#   fx3v1(x) menghitung pangkat tiga dari x,sebuah bilangan integer

#Realisasi
def fx3v1(x):
    return(x*x*x)

# Aplikasi
print (fx3v1(5))
print (fx3v1(-9))
print (fx3v1(3))

