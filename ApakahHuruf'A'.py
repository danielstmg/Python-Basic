# Deskripsi: Mengecek kebenaran dari character bila character tersebut
#            adalah huruf 'A'

# Defenisi dan spesifikasi dari fungsi IsAnA(x) adalah :
# IsAnA : character --> boolean
#   IsAnA(x) benar jika x adalah character (huruf) 'A'

# Realisasi
def IsAnA(x):
    return x == 'A'

# Aplikasi
print (IsAnA(1))
print (IsAnA(2))
print (IsAnA(-2))
print (IsAnA(-1))


